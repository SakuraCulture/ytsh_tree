package cn.iocoder.yudao.module.business.dal.dataobject.store;

import lombok.*;
import com.baomidou.mybatisplus.annotation.*;
import cn.iocoder.yudao.framework.mybatis.core.dataobject.BaseDO;

/**
 * 平台主档 DO
 *
 * ==============================================================
 * 【Why - 为什么要维护平台主档】
 * ==============================================================
 *
 * 设计决策：为什么要单独维护平台主档表？
 *
 * 方案A（枚举硬编码）：在代码中定义平台枚举
 * - 优点：简单，不需要额外的数据库表
 * - 缺点：
 *   1. 新增平台需要修改代码
 *   2. 无法动态管理平台配置
 *   3. 无法存储平台相关的配置信息
 *
 * 方案B（独立表）：拆出 platform_table
 * - 优点：
 *   1. 动态管理平台配置
 *   2. 存储平台相关配置信息
 *   3. 便于按平台维度统计和分析
 *   4. 支持平台下线等运营操作
 * - 缺点：需要 JOIN 查询（但频率较低，可接受）
 *
 * 最终选择：方案B
 * - 平台数量可能增加（如新增抖音、快手等）
 * - 不同平台有不同的配置和费率
 * - 需要按平台维度进行运营分析
 *
 * ==============================================================
 * 【What - 这个表存储什么】
 * ==============================================================
 * 存储外部平台（如美团、饿了么、抖音）的主档信息：
 * - 平台基本信息（名称、代码、类型）
 * - 平台状态
 * - 排序顺序
 *
 * ==============================================================
 * 【Constraints - 约束条件】
 * ==============================================================
 * - platformId：自增主键
 * - platformCode：平台代码，唯一，用于系统对接
 * - platformName：平台名称，展示用
 * - status：状态，0停用/1启用
 *
 * ==============================================================
 * 【Pitfalls - 已知陷阱与教训】
 * ==============================================================
 * - 【教训2024-05】平台下线后未处理关联数据
 *   → 问题：平台删除后，store_platform_table 中的关联数据仍存在
 *   → 问题：查询时可能关联到不存在的平台
 *   → 修复：
 *     1. 设置软删除，而非物理删除
 *     2. 删除平台时级联处理关联数据
 *     3. 查询时校验 platform 状态
 * - 【陷阱】platformCode 与实际平台代码不一致
 *   → 导致与平台对接失败
 *   → 建议：平台代码由平台方提供，不可随意修改
 * - 【风险】平台配置变更后未同步
 *   → 可能导致业务数据不一致
 *
 * @author 彼岸花
 */
@TableName("platform_table")
@KeySequence("platform_table_seq")
@Data
@EqualsAndHashCode(callSuper = true)
@ToString(callSuper = true)
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class PlatformDO extends BaseDO {

    /**
     * 平台ID
     *
     * 【What】数据库自增主键，唯一标识一个平台
     *
     * 【Constraints】
     * - 自增生成
     * - 全局唯一
     */
    @TableId
    private Long platformId;

    /**
     * 平台代码
     *
     * 【What】平台的唯一标识代码，用于系统对接
     *
     * 【Why - 为什么要用代码而不是名称】
     * - 平台代码通常由平台方定义，具有唯一性
     * - 便于与平台 API 对接
     * - 代码比名称更稳定，不会轻易变更
     *
     * 【Constraints】
     * - 最大20字符
     * - 唯一索引
     * - 不可修改
     *
     * 【Pitfalls】
     * - 【陷阱】platformCode 与实际平台代码不一致
     *   → 导致与平台对接失败
     *   → 建议：平台代码由平台方提供
     * - 【边界】平台代码可能包含特殊字符
     */
    private String platformCode;

    /**
     * 平台名称
     *
     * 【What】平台的展示名称
     *
     * 【Constraints】
     * - 最大50字符
     * - 唯一索引
     * - 用于展示给用户
     *
     * 【Pitfalls】
     * - 【边界】平台名称可能变更
     * - 【风险】名称变更后历史数据展示不一致
     */
    private String platformName;

    /**
     * 平台类型
     *
     * 【What】平台的业务类型
     *
     * 【Why - 为什么要区分平台类型】
     * - 不同类型的平台有不同的业务模式
     * - 便于按类型进行统计和分析
     *
     * 【Constraints】
     * - 最大20字符
     * - 可为空
     * - 建议枚举值：
     *   - MEITUAN - 美团
     *   - ELEM - 饿了么
     *   - DOUYIN - 抖音
     *   - KUAISHOU - 快手
     *   - JD - 京东
     *   - TAOBAO - 淘宝
     *
     * 【Pitfalls】
     * - 【风险】平台类型扩展时需同步修改
     */
    private String platformType;

    /**
     * 状态
     *
     * 【What】标识平台的可用状态
     *
     * 【Why - 为什么要控制平台状态】
     * - 平台可能下线或暂停合作
     * - 需要控制门店是否能够关联该平台
     *
     * 【Constraints】
     * - 0 - 停用（已下线或暂停合作）
     * - 1 - 启用（正常合作）
     * - 默认值为 1
     *
     * 【Pitfalls】
     * - 【陷阱】停用平台后，关联的门店如何处理？
     *   → 建议：停用平台时自动停用所有关联的门店平台关联
     * - 【风险】平台状态变更未同步到门店
     */
    private Integer status;

    /**
     * 排序顺序
     *
     * 【What】用于平台列表的展示顺序
     *
     * 【Constraints】
     * - 整数类型
     * - 越小越靠前
     * - 默认为0
     *
     * 【Pitfalls】
     * - 【边界】相同排序值的平台如何排序？
     *   → 按创建时间或名称排序
     */
    private Integer sortOrder;

}
