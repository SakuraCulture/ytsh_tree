package cn.iocoder.yudao.module.business.dal.dataobject.store;

import lombok.*;

import java.time.LocalDate;
import java.util.*;
import java.math.BigDecimal;
import java.math.BigDecimal;
import java.time.LocalDateTime;
import java.time.LocalDateTime;
import com.baomidou.mybatisplus.annotation.*;
import cn.iocoder.yudao.framework.mybatis.core.dataobject.BaseDO;

/**
 * 门店加盟商信息 DO
 *
 * ==============================================================
 * 【Why - 为什么要单独存储加盟商信息】
 * ==============================================================
 *
 * 设计决策：为什么要拆出独立的加盟商表，而不是直接存储在主表？
 *
 * 方案A（主表存储）：在 StoreDO 中直接存储 franchiseeName、franchiseePhone 等字段
 * - 优点：查询简单，减少 JOIN
 * - 缺点：
 *   1. 加盟商信息变更需要修改主表，每次变更都产生主表记录变化
 *   2. 加盟商可能同时关联多个门店，主表存储造成数据冗余
 *   3. 财务统计时需要单独关联，无法复用加盟商主数据
 *
 * 方案B（独立表）：拆出 store_franchisee_table
 * - 优点：
 *   1. 加盟商主数据独立，便于统一管理和变更历史追踪
 *   2. 支持一个加盟商关联多个门店
 *   3. 财务统计、合同管理可以复用加盟商信息
 * - 缺点：需要 JOIN 查询（但频率较低，可接受）
 *
 * 最终选择：方案B
 * - 业务上加盟商是独立实体，需要单独管理
 * - 变更频率与门店基础信息不同，独立存储便于版本管理
 * - 支持加盟商维度的统计分析
 *
 * ==============================================================
 * 【What - 这个表存储什么】
 * ==============================================================
 * 存储门店与加盟商的合作关系信息，包括：
 * - 加盟商基本信息（名称、联系方式）
 * - 财务信息（加盟费、保证金）
 * - 合同信息（合同期限）
 *
 * ==============================================================
 * 【Constraints - 约束条件】
 * ==============================================================
 * <ul>
 *   <li>franchiseeId：自增主键，非业务主键</li>
 *   <li>storeId：外键，关联 store_table.storeId</li>
 *   <li>franchiseeFee：加盟费，单位元，使用 BigDecimal 避免精度丢失</li>
 *   <li>securityDeposit：保证金，单位元</li>
 *   <li>contractStart/End：合同期限，contractEnd 必须 >= contractStart</li>
 * </ul>
 *
 * ==============================================================
 * 【Pitfalls - 已知陷阱与教训】
 * ==============================================================
 * <ul>
 *   <li>【教训2024-06】加盟商变更后未同步历史订单
 *       → 问题：门店更换加盟商后，历史订单仍显示原加盟商
 *       → 修复：订单表存储加盟商快照，不依赖实时查询
 *       → 风险：数据一致性需要额外保证</li>
 *   <li>【陷阱】franchiseeId 是自增主键，非业务主键
 *       → 注意：同一个加盟商可能关联多个门店，有多个 franchiseeId
 *       → 如需唯一标识加盟商，需用 franchiseeName + phone 组合</li>
 *   <li>【风险】加盟商合同到期后门店状态未自动变更
 *       → 需要定时任务检查并更新门店状态</li>
 *   <li>【边界】franchiseePhone 存储格式不统一
 *       → 建议：统一存储带国际区号格式，如 +86-138-0000-0000</li>
 *   <li>【教训2023-11】加盟费与保证金字段使用 Double 类型
 *       → 问题：高金额场景下精度丢失导致财务对账失败
 *       → 修复：改用 BigDecimal，保留2位小数</li>
 * </ul>
 *
 * @author 彼岸花
 * @see StoreDO
 */
@TableName("store_franchisee_table")
@KeySequence("store_franchisee_table_seq")
@Data
@EqualsAndHashCode(callSuper = true)
@ToString(callSuper = true)
@Builder
@NoArgsConstructor
@AllArgsConstructor
public class FranchiseeTableDO extends BaseDO {

    /**
     * 加盟ID
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 数据库自增主键，非业务主键
     *
     * ==============================================================
     * 【Why - 为什么要用自增主键】
     * ==============================================================
     * - franchiseeId 是技术主键，用于数据库唯一标识
     * - 业务上同一加盟商可能关联多个门店，因此不能作为业务主键
     * - 若需唯一标识加盟商，使用 franchiseeName + phone 组合
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>自增生成，无需手动赋值</li>
     *   <li>全局唯一</li>
     * </ul>
     */
    @TableId
    private Long franchiseeId;

    /**
     * 门店ID
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 外键，关联 store_table.storeId
     *
     * ==============================================================
     * 【Why - 为什么要冗余存储 storeId】
     * ==============================================================
     * - 便于快速查询某门店的加盟商信息
     * - 虽然可以通过 franchiseeId 查询关联门店，但 storeId 外键更直接
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>非空</li>
     *   <li>关联 store_table.storeId</li>
     * </ul>
     */
    private String storeId;

    /**
     * 加盟商名称
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟商的注册名称或常用名称
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>最大100字符</li>
     *   <li>允许重复（同一加盟商关联多个门店时）</li>
     *   <li>区分大小写，查询时需注意</li>
     * </ul>
     *
     * ==============================================================
     * 【Pitfalls - 陷阱】
     * ==============================================================
     * <ul>
     *   <li>【边界】同一公司多个品牌可能使用不同名称</li>
     *   <li>【风险】加盟商更名后历史数据不一致</li>
     * </ul>
     */
    private String franchiseeName;

    /**
     * 加盟联系方式
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟商的联系电话，支持手机号或座机
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>最大20字符</li>
     *   <li>建议格式：+86-手机号 或 区号-座机号</li>
     * </ul>
     *
     * ==============================================================
     * 【Pitfalls - 陷阱】
     * ==============================================================
     * <ul>
     *   <li>【边界】存储格式不统一，有手机号、座机、微信等多种形式</li>
     *   <li>【风险】联系信息变更后无法追溯历史</li>
     *   <li>【风险】无法验证号码真实性</li>
     * </ul>
     */
    private String franchiseePhone;

    /**
     * 加盟费
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟商需要支付的加盟费用，单位：元
     *
     * ==============================================================
     * 【Why - 为什么要单独存储加盟费】
     * ==============================================================
     * - 加盟费是财务统计的重要维度
     * - 不同加盟商、不同时期的加盟费可能不同
     * - 需要支持加盟费变更历史查询
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>使用 BigDecimal 类型，精度2位小数</li>
     *   <li>可为空（部分加盟模式可能免加盟费）</li>
     *   <li>单位：元</li>
     * </ul>
     *
     * ==============================================================
     * 【Pitfalls - 陷阱】
     * ==============================================================
     * <ul>
     *   <li>【教训】早期使用 Double 类型导致精度丢失
     *       → 修复：改用 BigDecimal</li>
     *   <li>【边界】加盟费为0的情况需特殊处理</li>
     * </ul>
     */
    private BigDecimal franchiseeFee;

    /**
     * 保证金
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟商缴纳的经营保证金，用于违约赔付，单位：元
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>使用 BigDecimal 类型，精度2位小数</li>
     *   <li>可为空（部分加盟模式可能免保证金）</li>
     *   <li>单位：元</li>
     * </ul>
     *
     * ==============================================================
     * 【Pitfalls - 陷阱】
     * ==============================================================
     * <ul>
     *   <li>【风险】保证金退还条件未明确记录</li>
     *   <li>【风险】保证金利息归属未明确</li>
     * </ul>
     */
    private BigDecimal securityDeposit;

    /**
     * 合同开始日期
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟合同的生效日期
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>格式：yyyy-MM-dd</li>
     *   <li>必须 <= contractEnd</li>
     *   <li>可为空（合同未签订时）</li>
     * </ul>
     */
    private LocalDate contractStart;

    /**
     * 合同结束日期
     *
     * ==============================================================
     * 【What - 字段含义】
     * ==============================================================
     * 加盟合同的到期日期
     *
     * ==============================================================
     * 【Constraints - 约束】
     * ==============================================================
     * <ul>
     *   <li>格式：yyyy-MM-dd</li>
     *   <li>必须 >= contractStart</li>
     *   <li>可为空（永久加盟或合同未签订时）</li>
     * </ul>
     *
     * ==============================================================
     * 【Pitfalls - 陷阱】
     * ==============================================================
     * <ul>
     *   <li>【风险】合同到期后门店权限未自动回收</li>
     *   <li>【风险】续签合同未更新此字段</li>
     *   <li>【边界】永久加盟合同的结束日期如何处理？</li>
     * </ul>
     */
    private LocalDate contractEnd;

}
