CREATE TABLE IF NOT EXISTS `biz_external_api_call_log` (
    `id` bigint NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    `platform_code` varchar(32) NOT NULL,
    `api_code` varchar(64) NOT NULL,
    `api_name` varchar(128) DEFAULT NULL,
    `biz_type` varchar(32) DEFAULT NULL,
    `biz_id` varchar(64) DEFAULT NULL,
    `biz_no` varchar(64) DEFAULT NULL,
    `trace_id` varchar(64) DEFAULT NULL,
    `external_trace_id` varchar(64) DEFAULT NULL,
    `request_id` varchar(64) DEFAULT NULL,
    `request_url` varchar(255) DEFAULT NULL,
    `request_method` varchar(16) DEFAULT NULL,
    `request_body` clob DEFAULT NULL,
    `response_body` clob DEFAULT NULL,
    `success` bit NOT NULL DEFAULT FALSE,
    `result_code` varchar(64) DEFAULT NULL,
    `result_msg` varchar(512) DEFAULT NULL,
    `duration_ms` int DEFAULT NULL,
    `merchant_code` varchar(64) DEFAULT NULL,
    `platform_store_id` varchar(64) DEFAULT NULL,
    `erp_store_code` varchar(64) DEFAULT NULL,
    `order_id` varchar(64) DEFAULT NULL,
    `channel_order_id` varchar(64) DEFAULT NULL,
    `creator` varchar(64) DEFAULT '',
    `create_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `updater` varchar(64) DEFAULT '',
    `update_time` timestamp NOT NULL DEFAULT CURRENT_TIMESTAMP,
    `deleted` bit NOT NULL DEFAULT FALSE,
    PRIMARY KEY (`id`)
);
CREATE INDEX IF NOT EXISTS `idx_biz_external_api_call_log_trace_id` ON `biz_external_api_call_log` (`trace_id`);
CREATE INDEX IF NOT EXISTS `idx_biz_external_api_call_log_external_trace_id` ON `biz_external_api_call_log` (`external_trace_id`);
